// Wizard state management
let currentStep = 1;
const totalSteps = 4;

/**
 * Show toast notification
 * @param {string} message - Toast message
 * @param {string} type - 'success' | 'error' | 'info'
 * @param {number} duration - Auto-hide duration in ms (default 5000)
 */
function showToast(message, type = 'info', duration = 8000) {
  const container = document.getElementById('toast-container');
  const toast = document.createElement('div');
  toast.className = `toast toast-${type}`;

  const icons = {
    success: '✓',
    error: '✕',
    info: 'ℹ'
  };

  toast.innerHTML = `
    <span class="toast-icon">${icons[type] || icons.info}</span>
    <span class="toast-message">${message}</span>
    <button class="toast-close" onclick="this.parentElement.remove()">✕</button>
  `;

  container.appendChild(toast);

  // Auto-hide after duration
  if (duration) {
    setTimeout(() => {
      toast.classList.add('hiding');
      toast.addEventListener('animationend', () => toast.remove());
    }, duration);
  }
}

/**
 * Update wizard progress to specific step
 * @param {number} step - Step number (1-4)
 */
function updateWizardProgress(step) {
  currentStep = step;

  for (let i = 1; i <= totalSteps; i++) {
    const section = document.getElementById('section-' + i);
    const stepElement = document.getElementById('step' + i);
    const indicator = document.getElementById('step' + i + '-ind');

    if (i < step) {
      // Completed sections
      section.classList.remove('active');
      stepElement.classList.add('completed');
      stepElement.classList.remove('active');
      indicator.classList.remove('active');
      indicator.classList.add('completed');
    } else if (i === step) {
      // Current section
      section.classList.add('active');
      stepElement.classList.remove('completed');
      stepElement.classList.add('active');
      indicator.classList.add('active');
      indicator.classList.remove('completed');
    } else {
      // Future sections
      section.classList.remove('active', 'completed');
      stepElement.classList.remove('active', 'completed');
      indicator.classList.remove('active', 'completed');
    }
  }
}

/**
 * Navigate to next step (no validation)
 */
function nextStep() {
  if (currentStep < totalSteps) {
    updateWizardProgress(currentStep + 1);
  }
}

/**
 * Navigate to previous step
 */
function prevStep() {
  if (currentStep > 1) {
    updateWizardProgress(currentStep - 1);
  }
}

/**
 * Validate Step 1 - Login credentials
 * Makes async request to /validate-login
 */
function validateStep1() {
  const urlBase = document.getElementById('urlBase').value.trim();
  const companyId = document.getElementById('companyId').value.trim();
  const usuario = document.getElementById('usuario').value.trim();
  const senha = document.getElementById('senha').value;

  // Validate required fields
  if (!urlBase || !companyId || !usuario || !senha) {
    showToast('Preencha todos os campos de login!', 'error');
    return;
  }

  const btn = document.querySelector('#section-1 .btn-primary');
  const originalText = btn.innerHTML;

  // Set loading state
  btn.classList.add('loading');
  btn.disabled = true;
  btn.innerHTML = '<span class="spinner"></span> Autenticando...';

  fetch('/validate-login', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ urlBase, companyId, usuario, senha })
  })
  .then(r => r.json().then(data => ({ ok: r.ok, data })))
  .then(({ ok, data }) => {
    // Reset button state
    btn.classList.remove('loading');
    btn.disabled = false;
    btn.innerHTML = originalText;

    if (ok && data.success) {
      // Success feedback
      showToast('Autenticação realizada com sucesso!', 'success', 2000);

      // If API URL was generated by backend, use it
      if (data.api_url) {
        document.getElementById('API_URL_GENERATED').value = data.api_url;
      } else {
        generateApiUrl();
      }

      // Small delay to show success message before advancing
      setTimeout(() => {
        updateWizardProgress(2);
      }, 300);
    } else {
      // Error feedback
      showToast('Erro: ' + (data.error || 'Falha no login'), 'error');
    }
  })
  .catch(() => {
    // Reset button state
    btn.classList.remove('loading');
    btn.disabled = false;
    btn.innerHTML = originalText;
    showToast('Erro na resposta do servidor', 'error');
  });
}

/**
 * Generate API URL from URL Base and Company ID
 * Format: {urlBase}/API/v1/?{companyName}/RPAManager
 */
function generateApiUrl() {
  const urlBase = document.getElementById('urlBase').value.trim();
  const companyId = document.getElementById('companyId').value.trim();
  const apiUrlField = document.getElementById('API_URL_GENERATED');

  if (!urlBase || !companyId) {
    apiUrlField.value = '';
    return;
  }

  // Extract domain from URL Base (e.g., https://planalto.goevo.net)
  let domain;
  try {
    const url = new URL(urlBase);
    domain = url.protocol + '//' + url.hostname;
    // Remove port if present
    domain = domain.replace(/:\d+$/, '');
  } catch (e) {
    domain = urlBase;
  }

  // Generate API URL: {domain}/API/v1/?{companyId}/RPAManager
  const apiUrl = `${domain}/API/v1/?${companyId}/RPAManager`;
  apiUrlField.value = apiUrl;
  return apiUrl;
}

/**
 * Load RPA versions from GoEvo API
 * @param {string} apiUrl - API URL
 * @param {string} token - Access token
 */
async function loadRpaVersions(apiUrl, token) {
  const selectElement = document.getElementById('RPA_VERSION');

  // Set loading state
  selectElement.disabled = true;
  selectElement.innerHTML = '<option value="">Carregando versões...</option>';

  try {
    const response = await fetch('/list-rpa-versions', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ api_url: apiUrl, token })
    });

    const result = await response.json();

    if (result.success && result.versions && result.versions.length > 0) {
      // Clear existing options (keep first option as fallback)
      selectElement.innerHTML = '<option value="">Selecione...</option>';

      // Add loaded versions
      result.versions.forEach(rpa => {
        const option = document.createElement('option');
        option.value = rpa.rpa_version;
        option.textContent = `${rpa.rpa_name} - ${rpa.rpa_version}`;
        selectElement.appendChild(option);
      });

      showToast(`${result.versions.length} versão(ões) RPA carregada(s)!`, 'success', 2000);
      return true;
    } else {
      // Error from API - show error but allow continuation
      const errorMsg = result.error || 'Erro ao carregar versões RPA';
      showToast(errorMsg, 'error');

      // Fallback to default option
      selectElement.innerHTML = '<option value="v1.0.0">v1.0.0 (Fallback)</option>';
      return false;
    }
  } catch (error) {
    console.error('Erro ao carregar RPA versions:', error);
    showToast('Erro de conexão ao carregar versões RPA', 'error');

    // Fallback to default option
    selectElement.innerHTML = '<option value="v1.0.0">v1.0.0 (Fallback)</option>';
    return false;
  } finally {
    // Reset loading state
    selectElement.disabled = false;
  }
}

/**
 * Validate Step 2 - API Configuration
 */
async function validateStep2() {
  const apiUrl = document.getElementById('API_URL_GENERATED').value.trim();
  const token = document.getElementById('GOEVO_APP_TPTOKEN').value.trim();

  if (!token) {
    showToast('Preencha o token de acesso!', 'error');
    return;
  }

  if (!apiUrl) {
    showToast('URL da API não gerada. Volte para etapa 1 e preencha os dados.', 'error');
    return;
  }

  // Load RPA versions before advancing to step 3
  const loaded = await loadRpaVersions(apiUrl, token);

  // Always advance to step 3 (even if fallback is used)
  updateWizardProgress(3);
}

/**
 * Validate Step 3 - Executor Configuration
 */
function validateStep3() {
  const executor = document.getElementById('RPA_EXECUTOR').value.trim();

  if (!executor) {
    showToast('Informe o nome do executor!', 'error');
    return;
  }

  updateWizardProgress(4);
}

/**
 * Validate and submit final step
 */
function validateStep4() {
  // Ensure API_URL hidden field is updated before submit
  const apiUrl = generateApiUrl();
  document.getElementById('API_URL').value = apiUrl || '';

  const form = document.getElementById('wizard-form');
  form.submit();
}

// Initialize wizard on DOM ready
document.addEventListener('DOMContentLoaded', function() {
  // Add listeners to generate API URL automatically
  const urlBase = document.getElementById('urlBase');
  const companyId = document.getElementById('companyId');

  if (urlBase) {
    urlBase.addEventListener('input', generateApiUrl);
  }
  if (companyId) {
    companyId.addEventListener('input', generateApiUrl);
  }

  // Generate initial API URL if fields are pre-filled
  generateApiUrl();
});
